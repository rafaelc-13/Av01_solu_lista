'''Faça um código que receba o valor da
base e da altura de um triângulo e
calcule sua área. usando a fórmula A =
(base x Altura ) /2'''

base = float(input("Valor da base do triângulo: "))
h = float(input("Valor da altura do triângulo: "))
A=(base*h)/2

print("A área do triângulo é: ",A)
