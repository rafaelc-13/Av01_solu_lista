'''Faça um código que receba 4
números e realize a soma deles e a
média entre eles. e mostre os
resultados'''

n1 = float(input('Digite 1ª Número: '))
n2 = float(input('Digite 2ª Número: '))
n3 = float(input('Digite 3ª Número: '))
n4 = float(input('Digite 4ª Número: '))
soma = n1 + n2 + n3 + n4
media = soma / 4

print(f"A soma dos valores é: {soma}, e a media: {media}.")

